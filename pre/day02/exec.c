/*
	1.现小敏有100元大钞,小伟有若干5元,2元,1元人民币
	问先小敏要与小伟等额交换,并要求交换的零钱中至少有5元,2元,1元人民币各一张
	有多少中交换方式
 */
#include <stdio.h>

int main(void)
{
	int n2, n1;
	int cnt = 0;

	for (n1 = 1; n1 <= 93; n1 ++) {
		for (n2 = 1; n2 <= 47; n2 ++) {
			if ((100-n1-n2*2 >= 5) && (100-n1-n2*2) % 5 == 0)			
				cnt ++;
		}	
	}

	printf("cnt:%d\n", cnt);

	return 0;
}

